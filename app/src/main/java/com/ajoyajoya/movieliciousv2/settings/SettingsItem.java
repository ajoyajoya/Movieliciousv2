package com.ajoyajoya.movieliciousv2.settings;

public class SettingsItem {

    private String idsetting;
    private String settingname;
    private String menudesc;
    private int iconsetting;
    private String switchsetting;

    public String getIdsetting() {
        return idsetting;
    }

    public void setIdsetting(String idsetting) {
        this.idsetting = idsetting;
    }

    public String getSettingname() {
        return settingname;
    }

    public void setSettingname(String settingname) {
        this.settingname = settingname;
    }

    public String getMenudesc() {
        return menudesc;
    }

    public void setMenudesc(String menudesc) {
        this.menudesc = menudesc;
    }

    public int getIconsetting() {
        return iconsetting;
    }

    public void setIconsetting(int iconsetting) {
        this.iconsetting = iconsetting;
    }

    public String getSwitchsetting() {
        return switchsetting;
    }

    public void setSwitchsetting(String switchsetting) {
        this.switchsetting = switchsetting;
    }



}
